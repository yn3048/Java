package loop.ex;

public class WhileEx2_1 {

	public static void main(String[] args) {
	
		int i = 10;
		int sum = 0;
		
		sum = sum + i;
		System.out.println("i = " + i + ", sum = "+ sum);
		
		i++;
		sum = sum + i;
		System.out.println("i = "+ i + ", sum = " + sum);
		
		i++;
		sum = sum + i;
		System.out.println("i = "+ i + ", sum = " + sum);
	}
}
