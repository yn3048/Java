package step1;

public class Main1_6 {
	public static void main(String[] args) {
		
		int A = 7;
		int B = 3;
		
		//두 자연수 A와 B가 주어진다. (1 ≤ A, B ≤ 10,000)
		
		if(A >= 1 && B <= 10000) return;
		
		System.out.println(A + B);
		System.out.println(A - B);
		System.out.println(A * B);
		System.out.println(A / B);
		System.out.println(A % B);
		
	}
}
